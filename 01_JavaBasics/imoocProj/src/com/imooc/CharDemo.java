package com.imooc;

public class CharDemo {

	public static void main(String[] args) {
		// 定义一个变量存放字符 'a'
		char a = 'a';
		System.out.println("a="+a);
		
		char ch = 65;
		System.out.println("ch = "+ch);
		
		char ch1 = 65535;
		System.out.println("ch1 = "+ch1);
		
		char ch2 = (char)65536;
		System.out.println("ch2 = "+ch2);

	}

}
